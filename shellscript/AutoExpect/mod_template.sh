function MOD_PRM {
KEY=$1
PRM=$2
CNF=$3

MATCH=`grep -n "^${KEY}" ${CNF}`
NUM=`echo ${MATCH}| awk -F ":" '{print $1}'`
sed "${NUM} s/.*/${KEY}=\"${PRM}\"/" ${CNF} > .tmp
cat .tmp > ${CNF}; rm -f .tmp
}

